from collections import defaultdict

inp = input().split(",")

d = defaultdict(list)

for i in inp:
    k = i.split(":")
    d[k[0]] = sorted(list(k[1]), reverse=True)

ans = ""

for i in d.keys():
    l = len(i)
    number = d[i]
    found = False
    for digit in number:
        if int(digit) - 1 < l:
            ans += i[int(digit) - 1]
            found = True
            break
    if not found:
        ans += "X"

print(ans)

# Input :
# Abhishek:34848,Mayur:4739,Friend:2949,Yeah:9889
# Output :
# kueX
